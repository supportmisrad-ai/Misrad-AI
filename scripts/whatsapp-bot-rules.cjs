function L({ h, d, f, btn, secs }) {
  const lines = [`header:\`${h||''}\`\\`, `description:\`${d||''}\`\\`,
    `footer:\`${f||''}\`\\`, `buttonText:\`${btn||''}\`\\`, 'sections:<'];
  for (const s of secs) {
    lines.push('(list)', `title:\`${s.t||''}\`\\`, 'rows:<');
    for (const i of s.i) {
      lines.push('(item)', `title:\`${i[0]}\`\\`, `description:\`${i[1]||''}\``, '(/item)');
    }
    lines.push('>', '(/list)');
  }
  return lines.join('\n') + '\n\n';
}


async function main() {
  const prisma = new PrismaClient();
  try {
    console.log('Seeding strategic_content...');

    const all = [...SCRIPTS, ...SYSTEM_SCRIPTS];
    let upserted = 0;

    for (const item of all) {
      await prisma.strategic_content.upsert({
        where: {
          module_id_category_title: {
            module_id: item.module_id,
            category: item.category,
            title: item.title,
          },
        },
        update: { content: item.content },
        create: item,
      });
      upserted++;
      process.stdout.write('.');
    }

    console.log(`\nDone! Upserted ${upserted} items.`);
  } catch (err) {
    console.error('Error:', err);
    process.exit(1);
  } finally {
    await prisma.$disconnect();
  }
}

main();
